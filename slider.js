const slideImage = document.getElementById("slideImage");
const slideTitle = document.getElementById("slideTitle");
const slideLocation = document.getElementById("slideLocation");
const slideText = document.getElementById("slideText");
const slideIndex = document.getElementById("slideIndex");

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function renderMarkup(value) {
  return escapeHtml(value)
    .replace(/\*{1,2}([^*]+?)\*{1,2}/g, "<strong>$1</strong>")
    .replace(/\r?\n/g, "<br>");
}

function stripMarkup(value) {
  return String(value ?? "").replace(/\*{1,2}([^*]+?)\*{1,2}/g, "$1");
}

function renderText(text) {
  slideText.innerHTML = "";

  const items = Array.isArray(text) ? text : [text];

  items.filter(Boolean).forEach((item) => {
    const paragraph = document.createElement("p");
    paragraph.className = "slide-text-item";
    paragraph.innerHTML = renderMarkup(item);
    slideText.appendChild(paragraph);
  });
}

function renderLocation(location) {
  const items = Array.isArray(location) ? location.filter(Boolean) : [location];
  slideLocation.innerHTML = items
    .map((item) => renderMarkup(item))
    .join("<br>");
}

function renderSlide(index) {
  const place = places[index];
  const plainTitle = stripMarkup(place.title);

  slideTitle.innerHTML = renderMarkup(place.title);
  renderLocation(place.location);
  renderText(place.text);
  slideIndex.textContent = `Карточка ${index + 1} из ${places.length}`;

  if (place.image) {
    slideImage.hidden = false;
    slideImage.src = place.image;
    slideImage.alt = `Фото: ${plainTitle}`;
  } else {
    slideImage.hidden = true;
    slideImage.removeAttribute("src");
    slideImage.alt = "";
  }
}

document.getElementById("prevBtn").addEventListener("click", () => {
  current = (current - 1 + places.length) % places.length;
  renderSlide(current);
});

document.getElementById("nextBtn").addEventListener("click", () => {
  current = (current + 1) % places.length;
  renderSlide(current);
});

renderSlide(current);
